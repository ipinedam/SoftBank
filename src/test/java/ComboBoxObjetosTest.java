    private void cbxClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxClienteItemStateChanged
        if (cbxCliente.getSelectedIndex() != -1) {
            Item item = (Item) cbxCliente.getSelectedItem();
            Cliente c = (Cliente) item.getValue();
            lblNombre.setText(c.getNombreCliente() + " " + c.getApellidosCliente());
        }
    }//GEN-LAST:event_cbxClienteItemStateChanged

