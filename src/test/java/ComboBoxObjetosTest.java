    private void rellenarComboBox() {
        EntityManagerFactory emf = new DBConnection().getEntityManagerFactory();
        ClienteDAO sdao = new ClienteDAO(emf);
        
        for (Cliente c : sdao.buscarCliente("")) {
            cbxCliente.addItem( new Item<Cliente>(c, c.getClaveIdentificacion()) );
        }
        cbxCliente.setSelectedIndex(-1);
        lblNombre.setText("");                
    }
    
