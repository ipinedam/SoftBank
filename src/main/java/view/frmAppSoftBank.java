    public void setFormMode(PanelMode formMode) {
        this.formMode = formMode;
        switch (this.formMode) {
            case CRUD:
                disableCRUDButtons();
                break;
            case SELECTION:
                this.setTitle(this.getTitle() + " (Consulta)");               
                ((pnlAppSoftBank) pnlBotones).setPanelMode(PanelMode.SELECTION);
                ((pnlAppSoftBank) pnlStatusBar).setPanelMode(PanelMode.READ_ONLY);                
                break;
            case READ_ONLY:
                this.setTitle(this.getTitle() + " (Visualización)");
                ((pnlAppSoftBank) pnlBotones).setPanelMode(PanelMode.READ_ONLY);
                ((pnlAppSoftBank) pnlStatusBar).setPanelMode(PanelMode.READ_ONLY);
                break;
            default:
                throw new AssertionError();
        }        
    }
    
