    protected void enableCRUDButtons() {
        if (formMode == PanelMode.CRUD) {
            btnInsertar.setEnabled(true);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }

    protected void disableCRUDButtons() {
        if (formMode == PanelMode.CRUD) {
            btnInsertar.setEnabled(false);
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }
    }
    
    protected void tryEnableCRUDButtons() {
        enableCRUDButtons();
        for (Component c : Functions.getComponents(this)) {
            if (c instanceof pnlAppSoftBank pasb) {
                if (!pasb.checkRequiredFields()) {
                    disableCRUDButtons();
                }
            }
        }
    }
    
